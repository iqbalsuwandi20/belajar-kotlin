fun main() {
    /**
     * if adalah salah satu kunci untuk percabangan
     * percabangan artinya bisa mengeksekusi program
     * kata kuncinya adalah if
     * 
     * else adalah kalo tidak terpenuhi dari percabangan
     * kata kuncinya adalah else
     * 
     * untuk menambah kondisi dari percabangan adalah menggunakan else if
     * kata kuncinya adalah else if
     * 
     */
    val nilai = 90

    if(nilai > 80) {
        println("Good Job")
    } else if (nilai > 70) {
        println("Not Bad")
    } else {
        println("Silahkan coba lagi tahun depan!")
    }
}